    private void writeReview(String orderId, String vendorName, String date, String cart) {
    // Get the currently logged-in customer's username
    String customerName = loggedIn.getUsername();

    // Create a JFrame for the review entry
    JFrame reviewFrame = new JFrame("Write Review");
    reviewFrame.setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);

    // Create JTextArea for the review content
    JTextArea reviewTextArea = new JTextArea(10, 50);
    JScrollPane scrollPane = new JScrollPane(reviewTextArea);

    // Create JButton to submit the review
    JButton submitButton = new JButton("Submit Review");

    // ActionListener for the submit button
    submitButton.addActionListener(new ActionListener() {
        @Override
        public void actionPerformed(ActionEvent e) {
            // Get the review content
            String reviewContent = reviewTextArea.getText();

            // Validate if the review content is not empty
            if (!reviewContent.isEmpty()) {
                // Write the review content to the reviews.txt file
                try (FileWriter fileWriter = new FileWriter(REVIEWS_PATH, true);
                     BufferedWriter bufferedWriter = new BufferedWriter(fileWriter);
                     PrintWriter printWriter = new PrintWriter(bufferedWriter)) {

                    // Format the review entry
                    String reviewEntry = orderId + ";" + customerName + ";" + vendorName + ";" + date + ";" + cart + ";" + reviewContent + ";";

                    // Write the review entry to the file
                    printWriter.println(reviewEntry);

                    // Show success message
                    JOptionPane.showMessageDialog(reviewFrame, "Review submitted successfully!", "Review Submitted", JOptionPane.INFORMATION_MESSAGE);

                    // Close the review frame
                    reviewFrame.dispose();
                } catch (IOException ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(reviewFrame, "Error writing review to file.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                // Show a warning if the review content is empty
                JOptionPane.showMessageDialog(reviewFrame, "Please enter your review before submitting.", "Empty Review", JOptionPane.WARNING_MESSAGE);
            }
        }
    });

    // Create a vertical BoxLayout for the JFrame
    Box verticalBox = Box.createVerticalBox();
    verticalBox.add(scrollPane);
    verticalBox.add(submitButton);

    // Add the Box to the JFrame
    reviewFrame.getContentPane().add(verticalBox);

    // Pack and center the JFrame
    reviewFrame.pack();
    reviewFrame.setLocationRelativeTo(null);
    reviewFrame.setVisible(true);
}

    private void reOrder(String orderId) {
        try (BufferedReader reader = new BufferedReader(new FileReader(ORDERS_PATH))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] orderDetails = line.split(";");

                // Check if the order matches the logged-in username
                if (orderDetails.length >= 1 && orderDetails[2].equals(orderId)) {
                    // Extract specific details for display

                    String cartString = orderDetails[4];
                    // Remove brackets and split the string by ", "
                    String[] elementsAsString = cartString.substring(1, cartString.length() - 1).split(", ");

                    // Create an int array and parse each element individually
                    int[] elements = new int[elementsAsString.length];
                    loggedIn.clearCart(); // Clear cart before adding reorder items
                    for (int i = 0; i < elementsAsString.length; i++) {
                        elements[i] = Integer.parseInt(elementsAsString[i].trim());
                        loggedIn.addToCart(elements[i]);
                        updateTotal();
                    }
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    
    private void fillOrderTable(DefaultTableModel orderModel) {
        // Read orders from the file and add matching orders to the table
        try (BufferedReader reader = new BufferedReader(new FileReader(ORDERS_PATH))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] orderDetails = line.split(";");

                // Check if the order matches the logged-in username
                if (orderDetails.length >= 1 && orderDetails[0].equals(loggedIn.getUsername())) {
                    // Extract specific details for display
                    String vendor = orderDetails[1];
                    String orderID = orderDetails[2];
                    String date = orderDetails[3];
                    String cartList = orderDetails[4];
                    String location = orderDetails[5];
                    String total = orderDetails[6];
                    String status = orderDetails[7];
                    String cart = (mapIdsToNames(cartList)).toString();
                    cart = cart.substring(1, cart.length() - 1);

                    try {
                            SimpleDateFormat originalFormat = new SimpleDateFormat("EEE MMM dd HH:mm:ss z yyyy");
                            Date tempDate = originalFormat.parse(date);

                            SimpleDateFormat newFormat = new SimpleDateFormat("dd/MM/yyyy HH:mm");
                            date = newFormat.format(tempDate);
                    } catch (ParseException e) {
                            e.printStackTrace();
                    }                    
                    

                    // Add the extracted details to the table model
                    orderModel.addRow(new String[]{orderID, vendor, date, location, cart, total, status});
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    
    private ArrayList<String> mapIdsToNames(String cartList) {
        ArrayList<String> cartNames = new ArrayList<>();
        try {
            Scanner scanner = new Scanner(new File(MENU_PATH));

            while (scanner.hasNextLine()) {
                String menuLine = scanner.nextLine();
                String[] parts = menuLine.split(";");

                for (int i = 1; i < parts.length; i++) {
                    String[] itemDetails = parts[i].split("\\|");

                    if (itemDetails.length == 4) {
                        int itemId = Integer.parseInt(itemDetails[0]);
                        String itemName = itemDetails[1].trim();

                        // Check if the itemId is present in the cartList
                        if (cartList.contains(Integer.toString(itemId))) {
                            cartNames.add(itemName);
                        }
                    }
                }
            }

            scanner.close();
        } catch (FileNotFoundException ex) {
            ex.printStackTrace();
        }

        return cartNames;
    }    

    private void updateCustomerPhone() {
        // Display an input dialog prompting the user to enter a location
        String newPhone = JOptionPane.showInputDialog(this, "Enter your phone number: ");

        // Check if the user pressed "OK" and entered a location
        if (newPhone != null && !newPhone.trim().isEmpty()) {
            loggedIn.setPhone(newPhone);
            lb_phone.setText(loggedIn.getPhone());
            
            try {
            // Read the content of the file
            File file = new File(CUST_CREDS_PATH);
            BufferedReader reader = new BufferedReader(new FileReader(file));
            StringBuilder content = new StringBuilder();
            String line;

            // Iterate through the lines, update the location, and build the new content
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(";");
                if (parts.length == 5) {
                    String username = parts[0];
                    String password = parts[1];
                    String balance = parts[2];
                    String location = parts[3];
                    String currentPhone = parts[4];
                    
                    if (username.equals(loggedIn.getUsername())) {
                        // Update the location for the specific user
                        currentPhone = newPhone;
                    }

                    // Reconstruct the line with the updated information
                    String updatedLine = username + ";" + password + ";" + balance + ";" + location + ";" + currentPhone;
                    content.append(updatedLine).append("\n");
                }
            }

            // Close the reader
            reader.close();

            // Write the updated content back to the file
            BufferedWriter writer = new BufferedWriter(new FileWriter(file));
            writer.write(content.toString());
            writer.close();

        } catch (IOException e) {
            e.printStackTrace();
            System.out.println("Error updating customer location.");
        }
        }
    }
    
